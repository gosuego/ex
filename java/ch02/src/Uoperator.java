
public class Uoperator {

	public static void main(String[] args) {
		int opr = 0;
		opr += 3;
		System.out.println(opr++);
		System.out.println(opr);
		System.out.println(++opr);
		System.out.println(opr);
		System.out.println(opr--);
		System.out.println(opr);
		System.out.println(--opr);
		System.out.println(opr);

	}

}
